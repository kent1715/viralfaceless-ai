// ─── Duration translation ────────────────────────────────────────
export function formatDuration(seconds: number, lang: Language): string {
  const m = Math.floor(seconds / 60);
  const s = Math.floor(seconds % 60);
  return lang === 'id'
    ? `${m} menit ${s} detik`
    : `${m}:${s.toString().padStart(2, '0')}`;
}
