#!/bin/bash
# =============================================================
# ViralFaceless AI - Complete Deploy Script
# Usage: scp deploy-viralfaceless.sh root@EC2_IP:/root/
#        Then on EC2: bash /root/deploy-viralfaceless.sh
# =============================================================
set -e

PROJECT_DIR="/root/viralfaceless-ai"

echo "=== ViralFaceless AI - Complete Deploy ==="
echo "Project dir: $PROJECT_DIR"

# Step 1: Extract all component files from base64-encoded archive
echo ""
echo "[Step 1/4] Extracting component files..."
mkdir -p "$PROJECT_DIR/src/components/viralfaceless"
mkdir -p "$PROJECT_DIR/src/components/ui"
mkdir -p "$PROJECT_DIR/src/lib"

cd "$PROJECT_DIR"

# Decode the embedded archive
python3 -c "
import base64, sys
data = sys.stdin.read().strip()
sys.stdout.buffer.write(base64.b64decode(data))
" < <(sed -n '/^#___ARCHIVE_START___$/,/^#___ARCHIVE_END___$/p' "$0" | grep -v '^#___') | tar xzf -

echo "  Files extracted successfully."

# Step 2: Ensure package.json has all needed dependencies
echo ""
echo "[Step 2/4] Checking package.json dependencies..."

cd "$PROJECT_DIR"

# Check and add missing dependencies
DEPS_TO_CHECK="bcryptjs jsonwebtoken framer-motion zustand next-themes class-variance-authority clsx tailwind-merge lucide-react @radix-ui/react-slot @radix-ui/react-separator @radix-ui/react-tabs @radix-ui/react-avatar @radix-ui/react-select @radix-ui/react-label z-ai-web-dev-sdk"

NEED_INSTALL=""
for dep in $DEPS_TO_CHECK; do
  if ! grep -q "\"$dep\"" package.json 2>/dev/null; then
    NEED_INSTALL="$NEED_INSTALL $dep"
  fi
done

if [ -n "$NEED_INSTALL" ]; then
  echo "  Installing missing deps:$NEED_INSTALL"
  bun add $NEED_INSTALL
else
  echo "  All dependencies present."
fi

# Step 3: Fix the build script if it references standalone
echo ""
echo "[Step 3/4] Fixing build script..."
if grep -q "standalone" package.json 2>/dev/null; then
  echo "  Removing standalone reference from build script..."
  sed -i 's|"build": "next build && cp -r .next/static .next/standalone/.next/ && cp -r public .next/standalone/"|"build": "next build"|g' package.json
  sed -i 's|"start": "NODE_ENV=production bun .next/standalone/server.js 2>&1 | tee server.log"|"start": "next start"|g' package.json
fi

# Also make sure the start script is just "next start"
node -e "
const fs = require('fs');
const pkg = JSON.parse(fs.readFileSync('package.json', 'utf8'));
let changed = false;
if (pkg.scripts.build !== 'next build') { pkg.scripts.build = 'next build'; changed = true; }
if (pkg.scripts.start !== 'next start') { pkg.scripts.start = 'next start'; changed = true; }
if (changed) { fs.writeFileSync('package.json', JSON.stringify(pkg, null, 2) + '\n'); console.log('  Fixed scripts: build=next build, start=next start'); }
else { console.log('  Scripts already correct.'); }
"

# Step 4: Rebuild Docker
echo ""
echo "[Step 4/4] Rebuilding Docker..."
cd "$PROJECT_DIR"
docker compose down 2>/dev/null || true
docker compose up -d --build

echo ""
echo "=== Deploy Complete! ==="
echo "Check logs with: docker compose logs -f"
echo "Or: docker logs viralfaceless-ai-web-1 -f"

exit 0

